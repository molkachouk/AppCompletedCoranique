import { createSlice } from "@reduxjs/toolkit";

const initialState = {
    groupsList: [],
    loading: false,
    error: null,
    response: null,
    statestatus: "idle",
};

const groupSlice = createSlice({
    name: 'group',
    initialState,
    reducers: {
        getRequest: (state) => {
            state.loading = true;
        },
        stuffDone: (state) => {
            state.loading = false;
            state.error = null;
            state.response = null;
            state.statestatus = "added";
        },
        getSuccess: (state, action) => {
            state.groupsList = action.payload;
            state.loading = false;
            state.error = null;
            state.response = null;
        },
        getFailed: (state, action) => {
            state.response = action.payload;
            state.loading = false;
            state.error = null;
        },
        getError: (state, action) => {
            state.loading = false;
            state.error = action.payload;
        },
        underGroupControl: (state) => {
            state.loading = false;
            state.response = null;
            state.error = null;
            state.statestatus = "idle";
        }
    },
});

export const {
    getRequest,
    getSuccess,
    getFailed,
    getError,
    underGroupControl,
    stuffDone,
} = groupSlice.actions;

export const groupReducer = groupSlice.reducer;